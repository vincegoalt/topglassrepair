export function getServiceIcon(serviceSlug: string) {
  const normalizedSlug = serviceSlug
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
  
  // Try to find exact match
  if (ServiceIcons[normalizedSlug as keyof typeof ServiceIcons]) {
    return ServiceIcons[normalizedSlug as keyof typeof ServiceIcons];
  }
  
  // Try to find partial match
  const iconKey = Object.keys(ServiceIcons).find(key => 
    normalizedSlug.includes(key) || key.includes(normalizedSlug)
  );
  
  return iconKey ? ServiceIcons[iconKey as keyof typeof ServiceIcons] : ServiceIcons.default;
}
